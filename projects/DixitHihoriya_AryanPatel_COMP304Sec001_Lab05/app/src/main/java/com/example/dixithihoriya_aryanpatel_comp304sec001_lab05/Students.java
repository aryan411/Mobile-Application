package com.example.dixithihoriya_aryanpatel_comp304sec001_lab05;

public class Students {

    String name;
    String id;

    public Students(){}

    public Students(String name, String id)
    {
        this.name = name;
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public String getId() {
        return id;
    }
}
